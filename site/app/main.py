def _human_bytes(n: int) -> str:
    """Human-readable size for the release catalog (KB/MB/GB)."""
    for unit, div in (("GB", 1_073_741_824), ("MB", 1_048_576), ("KB", 1_024)):
        if n >= div:
            return f"{n / div:.1f} {unit}"
    return f"{n} B"


def _release_manifest() -> dict:
    """Baked inventory of the full v1.0 parquet release (file/size/sha/provenance).

    Generated on the build host from the FreeNIC warehouse export dir (parquet
    dir + SHA256SUMS.txt + PROVENANCE.csv). The full release is NOT shipped in the
    container — this is the catalog with per-file data.freenic.org URLs.
    """
    path = DATA / "release_manifest.json"
    if not path.exists():
        return {"n_files": 0, "total_bytes": 0, "files": []}
    with open(path, encoding="utf-8") as fh:
        m = json.load(fh)
    for f in m.get("files", []):
        f["size_h"] = _human_bytes(f.get("bytes", 0))
        f["url"] = f"https://data.freenic.org/{f['file']}"
    m["total_h"] = _human_bytes(m.get("total_bytes", 0))
    return m


