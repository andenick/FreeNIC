@app.get("/api/failures")
def api_failures(
    year_from: int = Query(0, ge=0),
    year_to: int = Query(9999, ge=0),
    state: str = Query("", max_length=2),
    resolution_type: str = Query("", max_length=8),
    charter_class: str = Query("", max_length=8),
    limit: int = Query(5000, ge=1, le=10000),
) -> JSONResponse:
    """Filtered bank-failure records off the real slice (whole table is 4,115 rows,
    so the default cap returns every match). Every filter is bound (?). Returns the
    real columns; nulls stay null (never imputed)."""
    clauses = ["failure_year >= ?", "failure_year <= ?"]
    params: list = [year_from, year_to]
    if state.strip():
        clauses.append("state_code = ?")
        params.append(state.strip().upper())
    if resolution_type.strip():
        clauses.append("resolution_type = ?")
        params.append(resolution_type.strip().upper())
    if charter_class.strip():
        clauses.append("charter_class = ?")
        params.append(charter_class.strip().upper())
    where = " AND ".join(clauses)
    rows = _rows(
        "SELECT bank_name, city, state_code, CAST(closing_date AS VARCHAR) AS closing_date, "
        "failure_year, acquiring_institution, total_deposits, total_assets, estimated_loss, "
        "resolution_type, charter_class FROM bank_failures "
        f"WHERE {where} ORDER BY failure_year DESC, total_assets DESC NULLS LAST LIMIT ?",
        params + [limit],
    )
    return JSONResponse(rows)


