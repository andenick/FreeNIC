@app.post("/__track")
async def track_beacon(request: Request) -> Response:
    """First-party beacon sink for ark-track.js / ark-triad.js (TELEMETRY_STANDARD
    §4). The Carson telemetry ASGI middleware already records the request itself
    (service=freenic, path=/__track) into $CARSON_TELEMETRY_DB; this route just
    drains and acknowledges the beacon so it lands as a clean 204 instead of the
    404 the triad beacons previously hit (there was no /__track route). Same-origin
    sink mirrors deploy/shaikh (sibling FastAPI site) — no cookies, no PII, body is
    advisory only. See DATA_SERVING.md 'Telemetry' + TELEMETRY_STANDARD note."""
    try:
        await request.body()  # drain; content is advisory only
    except Exception:  # noqa: BLE001 — a beacon must never surface an error
        pass
    return Response(status_code=204)
