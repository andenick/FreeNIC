# Per-definition unit labels for the 163-year spine (UNITS DIFFER BY DEFINITION —
# the replicated spine deliberately does NOT harmonize units; a value series must
# never be spliced across definitions blindly). num_banks is a count for all defs.
_SPINE_DEFINITIONS = {
    "national_only":  {"label": "National banks only", "dollar_unit": "$ millions"},
    "member_banks":   {"label": "Fed member banks", "dollar_unit": "$ millions"},
    "all_insured":    {"label": "All FDIC-insured", "dollar_unit": "$ thousands"},
    "all_commercial": {"label": "All commercial banks", "dollar_unit": "$ billions"},
}
# The definitional junction years and what changes at each (drawn as vlines +
# annotations on the chart so a definitional break is never a silent splice).
_SPINE_JUNCTIONS = [
    {"year": 1896, "label": "1896 — state banks added"},
    {"year": 1914, "label": "1914 — Federal Reserve founded"},
    {"year": 1934, "label": "1934 — FDIC-insured universe"},
]
_SPINE_METRICS = ("num_banks", "total_deposits", "total_loans", "total_assets")


@app.get("/api/bank_aggregates")
def api_bank_aggregates(metric: str = Query("num_banks")) -> JSONResponse:
    """The replicated 163-year bank-aggregate spine for one metric, returned as one
    series PER DEFINITION so a definitional break is never a silent splice.

    Honest gaps: only the (year, definition) rows that exist in the spine are
    returned — missing years (e.g. national_only 1942–2025) come back as gaps, not
    interpolated. Units differ by definition and are NOT harmonized (see caption).
    """
    if metric not in _SPINE_METRICS:
        raise HTTPException(status_code=400, detail=f"Unknown metric: {metric}")
    rows = _rows(
        "SELECT year, definition, value FROM long_bank_aggregates_1863_2026 "
        "WHERE metric = ? ORDER BY definition, year",
        [metric],
    )
    # group into one ordered (x, y) series per definition present for this metric
    series: dict[str, dict] = {}
    for r in rows:
        d = r["definition"]
        s = series.setdefault(d, {"definition": d,
                                  "label": _SPINE_DEFINITIONS.get(d, {}).get("label", d),
                                  "dollar_unit": _SPINE_DEFINITIONS.get(d, {}).get("dollar_unit", ""),
                                  "x": [], "y": []})
        s["x"].append(r["year"])
        s["y"].append(r["value"])
    # stable, chronological-by-coverage order of the definitions
    order = ["national_only", "member_banks", "all_insured", "all_commercial"]
    out_series = [series[d] for d in order if d in series]
    return JSONResponse({
        "metric": metric,
        "metrics": list(_SPINE_METRICS),
        "is_count": metric == "num_banks",
        "junctions": _SPINE_JUNCTIONS,
        "series": out_series,
    })


@app.get("/api/lookup_institution")
def api_lookup_institution(
    q: str = Query("", min_length=0),
    entity_type: str = Query("", max_length=8),
    state: str = Query("", max_length=2),
    limit: int = Query(50, ge=1, le=200),
) -> JSONResponse:
    """Parameterized search over the real active-institutions slice.

    Matches the query against the legal/short name OR the RSSD id (when the query
    is numeric), with optional exact filters on entity_type and state_code. Every
    term is bound (?), never interpolated. Returns the trimmed real columns the
    slice carries — nothing derived or fabricated (date_established is not
    populated in the slice, so it comes back as-is and the UI shows it honestly).
    """
    q = q.strip()
    clauses: list[str] = []
    params: list = []
    if q:
        like = f"%{q}%"
        if q.isdigit():
            clauses.append(
                "(lower(name_legal) LIKE lower(?) OR lower(name_short) LIKE lower(?) "
                "OR CAST(rssd_id AS VARCHAR) = ? OR CAST(fdic_cert AS VARCHAR) = ?)"
            )
            params += [like, like, q, q]
        else:
            clauses.append("(lower(name_legal) LIKE lower(?) OR lower(name_short) LIKE lower(?))")
            params += [like, like]
    if entity_type.strip():
        clauses.append("entity_type = ?")
        params.append(entity_type.strip().upper())
    if state.strip():
        clauses.append("state_code = ?")
        params.append(state.strip().upper())
    if not clauses:
        return JSONResponse([])
    where = " AND ".join(clauses)
    rows = _rows(
        "SELECT rssd_id, name_legal, name_short, entity_type, charter_type, city, "
        "state_code, fdic_cert, CAST(date_established AS VARCHAR) AS date_established "
        f"FROM institutions_active WHERE {where} "
        "ORDER BY name_legal LIMIT ?",
        params + [limit],
    )
    return JSONResponse(rows)


