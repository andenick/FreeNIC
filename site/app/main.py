def _schedule_slug(form_family: str, schedule: str) -> str:
    """Filesystem/URL-safe slug for a schedule's per-schedule CSV file."""
    return f"{form_family}_{schedule}".replace("/", "-").replace(" ", "_")


def _dict_schedule_index() -> dict[str, list[dict]]:
    """Per-family list of {schedule, n_items, n_codes} from the line-item table.

    Drives the taxonomy-tree landing and the schedule enumeration. Keyed by
    form_family ('y9c' / 'call').
    """
    rows = _rows(
        "SELECT form_family, schedule, COUNT(*) AS n_items, "
        "COUNT(DISTINCT mdrm_code) AS n_codes "
        "FROM dict_schedule_lineitems WHERE schedule IS NOT NULL "
        "GROUP BY form_family, schedule ORDER BY form_family, schedule"
    )
    out: dict[str, list[dict]] = {}
    for r in rows:
        out.setdefault(r["form_family"], []).append(r)
    return out


def _schedule_one_liner(form_family: str, schedule: str) -> str:
    """One-line description for a schedule (from explanations.json title)."""
    blk = EXPLANATIONS.get("schedules", {}).get(schedule, {})
    return blk.get("title", "")


def _dict_search(q: str, page: int) -> tuple[list[dict], int]:
    """Server-side search over the dict taxonomy slice.

    Searches mdrm/UBPR codes, item descriptions/captions, and schedule names over
    the union of `dict_schedule_lineitems` and `dict_ubpr_concepts`. Returns one row
    per (code) with family/schedule/line/caption + a count of taxonomy relationships
    that reference the code (the "validated-relationship count"). Parameterized —
    the search term is bound, never interpolated. Paginated at _DICT_PAGE_SIZE.
    """
    like = f"%{q.strip()}%"
    # Per-code relationship counts: a relationship references a code when its
    # `codes` list contains that code. We pre-aggregate validated relationships
    # (status confirms the edit holds in the bulk data) per code via a join table.
    base = """
        WITH lineitems AS (
            SELECT mdrm_code AS code, form_family AS family, schedule, line_number AS line,
                   item_description AS caption, 'schedule' AS kind
            FROM dict_schedule_lineitems
            WHERE mdrm_code IS NOT NULL AND mdrm_code <> ''
        ),
        ubpr AS (
            SELECT ubpr_code AS code, 'ubpr' AS family, category AS schedule, NULL AS line,
                   caption, 'ubpr' AS kind
            FROM dict_ubpr_concepts
            WHERE ubpr_code IS NOT NULL AND ubpr_code <> ''
        ),
        catalog AS (
            SELECT code, ANY_VALUE(family) AS family, ANY_VALUE(schedule) AS schedule,
                   ANY_VALUE(line) AS line, ANY_VALUE(caption) AS caption,
                   ANY_VALUE(kind) AS kind
            FROM (SELECT * FROM lineitems UNION ALL SELECT * FROM ubpr)
            GROUP BY code
        ),
        relcounts AS (
            SELECT c.code,
                   COUNT(*) FILTER (WHERE r.status IN
                       ('CONFIRMED','CONFIRMED_CURRENT','VINTAGE_CONFIRMED','LOW_N_PASS')
                   ) AS n_validated
            FROM catalog c
            JOIN dict_relationships_summary r
              ON r.codes IS NOT NULL AND position(c.code IN r.codes) > 0
            GROUP BY c.code
        )
        SELECT cat.code, cat.family, cat.schedule, cat.line, cat.caption, cat.kind,
               COALESCE(rc.n_validated, 0) AS n_validated
        FROM catalog cat
        LEFT JOIN relcounts rc ON rc.code = cat.code
        WHERE (? = '' OR lower(cat.code) LIKE lower(?) OR lower(cat.caption) LIKE lower(?)
               OR lower(COALESCE(cat.schedule,'')) LIKE lower(?))
    """
    count_sql = f"SELECT COUNT(*) AS n FROM ({base}) _c"
    params = [q.strip(), like, like, like]
    total = _rows(count_sql, params)[0]["n"]
    offset = max(0, page - 1) * _DICT_PAGE_SIZE
    page_sql = base + " ORDER BY cat.code LIMIT ? OFFSET ?"
    rows = _rows(page_sql, params + [_DICT_PAGE_SIZE, offset])
    for r in rows:
        r["ref_url"] = _dict_ref_url(r.get("family"), r.get("schedule"))
    return rows, total


@app.get("/dictionary", response_class=HTMLResponse)
def dictionary(
    request: Request,
    q: str = Query("", max_length=120),
    page: int = Query(1, ge=1),
) -> HTMLResponse:
    """Taxonomy-tree landing + server-side variable search over the dict slice.

    When a query is present, the search-results grid is shown; the taxonomy tree
    (source families -> schedules) is always rendered as the browse surface.
    """
    rows, total = _dict_search(q, page) if q.strip() else ([], 0)
    n_pages = max(1, -(-total // _DICT_PAGE_SIZE)) if q.strip() else 1
    page = min(page, n_pages)
    meta = _dict_meta()

    # Build the taxonomy tree: each source family with its schedules (or a single
    # layer card for derivation layers that have no line-item schedules).
    sched_index = _dict_schedule_index()
    families: list[dict] = []
    n_schedules = 0
    for fam in _DICT_FAMILIES:
        entry = {
            "key": fam["key"],
            "label": fam["label"],
            "kind": fam["kind"],
            "category": EXPLANATIONS.get("categories", {}).get(fam["key"], {}),
        }
        if fam["kind"] == "schedules":
            scheds = []
            for s in sched_index.get(fam["family"], []):
                scheds.append({
                    "schedule": s["schedule"],
                    "n_items": s["n_items"],
                    "n_codes": s["n_codes"],
                    "title": _schedule_one_liner(fam["family"], s["schedule"]),
                    "code": _schedule_slug(fam["family"], s["schedule"]),
                })
            entry["schedules"] = scheds
            n_schedules += len(scheds)
        else:
            entry["layer"] = EXPLANATIONS.get("layers", {}).get(fam["layer"], {})
            entry["layer_key"] = fam["layer"]
        families.append(entry)

    # Headline numbers (preserved + extended).
    n_codes = _rows(
        "SELECT COUNT(*) AS n FROM ("
        "  SELECT mdrm_code AS c FROM dict_schedule_lineitems WHERE mdrm_code <> '' "
        "  UNION SELECT ubpr_code FROM dict_ubpr_concepts WHERE ubpr_code <> '') _u"
    )[0]["n"]
    n_rels = _rows("SELECT COUNT(*) AS n FROM dict_relationships_summary")[0]["n"]

    return templates.TemplateResponse(
        "dictionary.html",
        {
            "request": request,
            "q": q,
            "rows": rows,
            "total": total,
            "page": page,
            "n_pages": n_pages,
            "page_size": _DICT_PAGE_SIZE,
            "meta": meta,
            "ref_base": DICT_REF_BASE,
            "families": families,
            "n_schedules": n_schedules,
            "n_codes": n_codes,
            "n_rels": n_rels,
        },
    )


# ---------------- /dictionary/schedule/{code} — one schedule page ----------------

def _parse_schedule_slug(code: str) -> tuple[str, str] | None:
    """Resolve a slug like 'call_RC' / 'y9c_HC-B' to (form_family, schedule)."""
    if "_" not in code:
        return None
    fam, _, sched = code.partition("_")
    fam = fam.strip().lower()
    sched = sched.replace("-", "-").strip().upper()
    if fam not in ("call", "y9c"):
        return None
    hit = _rows(
        "SELECT DISTINCT form_family, schedule FROM dict_schedule_lineitems "
        "WHERE form_family = ? AND upper(schedule) = ?",
        [fam, sched],
    )
    if not hit:
        return None
    return hit[0]["form_family"], hit[0]["schedule"]


def _schedule_lineitems(form_family: str, schedule: str) -> list[dict]:
    """Full ordered line-item grid for a schedule (the columns the table has)."""
    return _rows(
        "SELECT line_number, mdrm_code, item_description, column_role, forms, "
        "start_date, notes FROM dict_schedule_lineitems "
        "WHERE form_family = ? AND schedule = ? "
        "ORDER BY try_cast(line_number AS DOUBLE) NULLS LAST, line_number, mdrm_code",
        [form_family, schedule],
    )


def _schedule_relationship_highlights(codes: list[str], limit: int = 8) -> list[dict]:
    """Top validated/identity relationships that touch this schedule's codes.

    A relationship 'touches' the schedule when its pipe-delimited `codes` list
    contains any of the schedule's MDRM codes. Ranked by empirical n (coverage),
    preferring identity/ratio kinds with a real pass rate.
    """
    if not codes:
        return []
    # Build an OR of position() probes — codes are bound, never interpolated.
    probes = " OR ".join(["position(? IN codes) > 0"] * len(codes))
    sql = (
        "SELECT rel_id, kind, status, expression, empirical_pass_rate, empirical_n "
        "FROM dict_relationships_summary "
        f"WHERE codes IS NOT NULL AND ({probes}) "
        "AND empirical_pass_rate IS NOT NULL "
        "ORDER BY try_cast(empirical_n AS BIGINT) DESC NULLS LAST LIMIT ?"
    )
    rows = _rows(sql, list(codes) + [limit])
    for r in rows:
        try:
            r["pass_pct"] = f"{float(r['empirical_pass_rate']) * 100:.1f}%"
        except (TypeError, ValueError):
            r["pass_pct"] = "—"
        try:
            r["n_h"] = f"{int(r['empirical_n']):,}"
        except (TypeError, ValueError):
            r["n_h"] = "—"
    return rows


# The warehouse view a schedule's data is exposed through (access info).
_SCHEDULE_VIEW = {
    ("call", "RC"): "sched_call_rc", ("call", "RC-B"): "sched_call_rc_b",
    ("call", "RC-C"): "sched_call_rc_c", ("call", "RC-E"): "sched_call_rc_e",
    ("call", "RC-N"): "sched_call_rc_n", ("call", "RC-R"): "sched_call_rc_r",
    ("call", "RI"): "sched_call_ri",
}


def _schedule_view_name(form_family: str, schedule: str, codes: list[str]) -> str:
    """The shaped warehouse view that exposes this schedule, if known.

    Prefer the access map's recorded shaped view for the schedule's codes (the
    real, data-derived mapping); fall back to the static Call map.
    """
    if codes:
        probes = ",".join(["?"] * len(codes))
        hit = _rows(
            f"SELECT shaped_views, COUNT(*) AS n FROM dict_variable_access_map "
            f"WHERE variable_id IN ({probes}) AND shaped_views <> '' "
            "GROUP BY shaped_views ORDER BY n DESC LIMIT 1",
            list(codes),
        )
        if hit and hit[0]["shaped_views"]:
            # shaped_views may be a comma/pipe list — take the first entry.
            first = re.split(r"[,|]", hit[0]["shaped_views"])[0].strip()
            if first:
                return first
    return _SCHEDULE_VIEW.get((form_family, schedule), "")


@app.get("/dictionary/schedule/{code}", response_class=HTMLResponse)
def dictionary_schedule(request: Request, code: str) -> HTMLResponse:
    """One schedule page: explanation, full line-item grid, relationship
    highlights, access info, and a per-schedule download corner."""
    parsed = _parse_schedule_slug(code)
    if parsed is None:
        raise HTTPException(status_code=404, detail=f"Unknown schedule: {code}")
    form_family, schedule = parsed
    slug = _schedule_slug(form_family, schedule)
    items = _schedule_lineitems(form_family, schedule)
    codes = sorted({r["mdrm_code"] for r in items if r.get("mdrm_code")})
    explain = EXPLANATIONS.get("schedules", {}).get(schedule, {})
    rels = _schedule_relationship_highlights(codes)
    view = _schedule_view_name(form_family, schedule, codes)
    fam_label = {"call": "Call Report", "y9c": "FR Y-9C"}.get(form_family, form_family)
    dict_parquet = (
        "https://data.freenic.org/dict_schedule_lineitems.parquet"
    )
    return templates.TemplateResponse(
        "schedule.html",
        {
            "request": request,
            "form_family": form_family,
            "fam_label": fam_label,
            "schedule": schedule,
            "slug": slug,
            "items": items,
            "n_items": len(items),
            "n_codes": len(codes),
            "explain": explain,
            "rels": rels,
            "view": view,
            "csv_url": f"/static/dictionary_csv/{slug}.csv",
            "dict_parquet": dict_parquet,
            "ref_url": _dict_ref_url(form_family, schedule),
            "meta": _dict_meta(),
        },
    )


# ---------------- /dictionary/variable/{mnemonic} — one variable page ----------------

def _variable_record(mnemonic: str) -> dict | None:
    """Resolve a variable mnemonic to its dict record (line-item OR UBPR).

    Returns None for unknown codes (the route 404s cleanly).
    """
    code = mnemonic.strip().upper()
    li = _rows(
        "SELECT form_family, schedule, line_number, item_description, column_role, "
        "mdrm_code, forms, start_date, notes FROM dict_schedule_lineitems "
        "WHERE upper(mdrm_code) = ? "
        "ORDER BY try_cast(line_number AS DOUBLE) NULLS LAST LIMIT 1",
        [code],
    )
    if li:
        r = li[0]
        return {
            "kind": "lineitem", "code": r["mdrm_code"],
            "name": r["item_description"], "form_family": r["form_family"],
            "schedule": r["schedule"], "line_number": r["line_number"],
            "column_role": r["column_role"], "forms": r["forms"],
            "start_date": r["start_date"], "notes": r["notes"],
            "schedule_slug": _schedule_slug(r["form_family"], r["schedule"])
            if r["schedule"] else "",
        }
    ub = _rows(
        "SELECT ubpr_code, caption, category, derivation, inputs, validated, "
        "empirical_agreement_rate, notes FROM dict_ubpr_concepts "
        "WHERE upper(ubpr_code) = ? LIMIT 1",
        [code],
    )
    if ub:
        r = ub[0]
        return {
            "kind": "ubpr", "code": r["ubpr_code"], "name": r["caption"],
            "category": r["category"], "derivation": r["derivation"],
            "inputs": r["inputs"], "validated": r["validated"],
            "agreement": r["empirical_agreement_rate"], "notes": r["notes"],
        }
    return None


def _variable_access(code: str) -> list[dict]:
    """Which base table / views expose a variable (from the access map)."""
    return _rows(
        "SELECT base_table, n_obs, raw_view, shaped_views FROM dict_variable_access_map "
        "WHERE upper(variable_id) = ?",
        [code.strip().upper()],
    )


def _variable_relationships(code: str, limit: int = 25) -> list[dict]:
    """Relationships whose codes list references this variable."""
    rows = _rows(
        "SELECT rel_id, scope, kind, status, expression, empirical_pass_rate, "
        "empirical_n FROM dict_relationships_summary "
        "WHERE codes IS NOT NULL AND position(? IN codes) > 0 "
        "ORDER BY try_cast(empirical_n AS BIGINT) DESC NULLS LAST LIMIT ?",
        [code.strip().upper(), limit],
    )
    for r in rows:
        try:
            r["pass_pct"] = f"{float(r['empirical_pass_rate']) * 100:.1f}%"
        except (TypeError, ValueError):
            r["pass_pct"] = "—"
    return rows


